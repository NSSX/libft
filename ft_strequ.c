#include "libft.h"

int	ft_strequ(char const *s1, char const *s2)
{
	int index;

	index = 0;
	while (s1[index] != '\0' && s2[index] != '\0' && s1[index] == s2[index])
		index++;
	if (s1[index] == '\0' && s2[index] == '\0')
		return (1);
	else
		return (0);
}
